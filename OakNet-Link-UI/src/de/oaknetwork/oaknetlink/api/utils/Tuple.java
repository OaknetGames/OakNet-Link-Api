package de.oaknetwork.oaknetlink.api.utils;

/**
 * This class saves a tuple of two
 * 
 * @author StackOverflow
 */
public class Tuple<X, Y> { 
	  public final X value1; 
	  public final Y value2; 
	  public Tuple(X value1, Y value2) { 
	    this.value1 = value1; 
	    this.value2 = value2; 
	  } 
	} 