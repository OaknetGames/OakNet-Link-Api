package de.oaknetwork.oaknetlink.api.gui.components;

/**
 * This represents an item which can be put in a ComboBox
 * 
 * @author Fabian Fila
 */
public class ComboBoxItem {

	public final String itemName;

	/**
	 * Creates a new ComboBoxItem with the given name
	 * 
	 * @param itemName
	 */
	public ComboBoxItem(String itemName) {
		this.itemName = itemName;
	}

}
